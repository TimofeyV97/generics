package app.m0.e1;


public class StringTuple {

    private final String left;

    private final String right;

    public StringTuple(final String left, final String right) {
        this.left = left;
        this.right = right;
    }

    public String getLeft() {
        return this.left;
    }

    public String getRight() {
        return this.right;
    }

}
